export function round(a){
    return Math.round(a);
}
export function ceil(a){
    return Math.ceil(a);
}
export function floor(a){
    return Math.floor(a);
}
export function trunc(a){
    return Math.trunc(a);
}
export function power(x,y){
    return Math.pow(x,y);
}
export function randomNumber(x){
    return Math.floor((Math.random() * x) + 1);
}
